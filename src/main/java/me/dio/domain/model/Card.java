package me.dio.domain.model;


public class Card {
    private String number;
    private float limit;

    // Getters and Setters
    public String getNumber() {
        return number;
    }
    public void setNumber(String number) {
        this.number = number;
    }

    public float getLimit() {
        return limit;
    }
    public void setLimit(float limit) {
        this.limit = limit;
    }
}

